#include <iostream>   // Library for input and output
using namespace std;  // Allows us to write cout instead of std::cout

int main()            // Main function where program starts
{
    int i = 1;        // Start value of i is 1 (changed from 0)

    while (i < 5)     // Loop runs while i is less than 5
    {
        cout << "Usama Siraj" << endl;   // Print the name
        i++;         // Increase value of i by 1 each time
    }

    return 0;        // End the program.
}